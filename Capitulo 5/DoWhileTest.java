// figura 5.7: DoWhileTest.java
// instrução de repetição do...while

public class DoWhileTest{
	public static void main(String[] args) {
		int contador = 1;
		
		do{
			System.out.printf("%d ", contador);
			contador++;
		}while(contador <=10);

		System.out.println();
	}
}// fim da classe DoWhileTest